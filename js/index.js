function onepag() {
    var a = document.getElementById('one').getElementsByTagName('p');
    var b = document.getElementById('tab').getElementsByTagName('div');
    for (var i = 0; i < a.length; i++) {
        a[i].o = i;
        a[0].className="pshow";
        a[i].onmouseover = function () {
            for (var n = 0; n < a.length; n++) {
                b[n].className = "hide";
                a[n].className = "";
            }
            b[this.o].className = "show";
            a[this.o].className = "pshow";
        }
    }
}
